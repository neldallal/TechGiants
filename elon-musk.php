                <p>
                   Tesla, Inc. (originally Tesla Motors) was incorporated in July 2003 by Martin Eberhard and Marc Tarpenning, who financed the company until the Series A round of funding. Both men played active roles in the company's early development prior to Musk's involvement. Musk led the Series A round of investment in 2004, joining Tesla's board of directors as its chairman. Musk took an active role within the company and oversaw Roadster product design but was not deeply involved in day-to-day business operations. Following a series of escalating conflicts in 2007 and the 2008 financial crisis, Eberhard was ousted from the firm. Musk assumed leadership of the company as CEO and product architect in 2008, positions he still holds today. A 2009 lawsuit settlement with Eberhard designated Musk as a Tesla co-founder, along with Tarpenning and two others. As of 2019, Musk is the longest tenured CEO of any automotive manufacturer globally. 
                </p>
                <p>
                    Tesla first built an electric sports car, the Tesla Roadster, in 2008. With sales of about 2,500 vehicles, it was the first serial production all-electric car to use lithium-ion battery cells. Tesla began delivery of its four-door Model S sedan in 2012; a cross-over, the Model X was launched in 2015. A mass market sedan, the Model 3 was released in 2017. As of March 2020, it is the world's best-selling electric car, with more than 500,000 units delivered. A fifth vehicle, the Model Y crossover, was launched in 2020.[106] The Cybertruck, an all-electric pickup truck, was unveiled in 2019. Under Musk, Tesla has also constructed multiple lithium-ion battery and electric vehicle subassembly factories, such as Gigafactory 1 in Nevada and Gigafactory 3 in China.
                </p>
                <p>
                    Since its initial public offering in 2010, Tesla stock has risen significantly; it became the most valuable carmaker in summer 2020. It entered the S&P 500 later that year.
                </p>
            </section>

            <section class="box-elon">
                <h4>SpaceX</h4>
                
                <figure class="spacex-fig">
                <img class="flexible" src="images/spacex.png" alt="Image failed to load.">
                <figcaption>SpaceX's first rocket launch</figcaption>
                </figure>

                <p>
                    In 2001, Musk conceived Mars Oasis: a miniature greenhouse on Mars would grow food crops and reawaken public interest in space exploration. In October 2001, Musk traveled with a group to Moscow to buy refurbished Dnepr Intercontinental ballistic missiles (ICBMs) that could send the greenhouse payloads into space. He met with companies such as NPO Lavochkin and Kosmotras; however, Musk was seen as a novice and was even spat on by one of the Russian chief designers. The group returned to the United States empty-handed. In February 2002, the group returned to Russia to look for three ICBMs. They had another meeting with Kosmotras and were offered one rocket for $8 million, which Musk rejected. Musk instead decided to start a company that could build affordable rockets. With $100 million of his early fortune, Musk founded Space Exploration Technologies Corp., traded as SpaceX, in May 2002.
                </p>
                <p>
                    After three failed launches, SpaceX succeeded in launching the Falcon 1 in 2008. It was the first private liquid-fuel rocket to reach Earth orbit. Later that year, SpaceX received a $1.6 billion Commercial Resupply Services program contract for 12 flights of its Falcon 9 rocket and Dragon spacecraft to the International Space Station, replacing the Space Shuttle after its 2011 retirement. In 2012, the Dragon vehicle berthed with the ISS, a first for a private enterprise. Working towards its goal of reusable rockets, in 2015, SpaceX successfully landed the first stage of a Falcon 9. Landings were later achieved on an autonomous spaceport drone ship, an ocean-based recovery platform. In 2018, SpaceX launched the Falcon Heavy; the inaugural mission carried a Tesla Roadster as a dummy payload. In 2017, SpaceX unveiled its next-generation launch vehicle and spacecraft system, Big Falcon Rocket (BFR), which would support all SpaceX launch service provider capabilities. In 2018, SpaceX announced a planned 2023 lunar circumnavigation mission, a private flight called #<em>dearMoon project</em>. Two years later, SpaceX launched its first manned flight, the Demo-2, becoming the first private company to place a person into orbit and dock a crewed space-craft with the ISS.
                </p>
                <p>
                    SpaceX began development of the Starlink constellation of low Earth orbit satellites in 2015 to provide satellite Internet access, with the first two prototype satellites launched in February 2018. A second set of test satellites and the first large deployment of a piece of the constellation occurred in May 2019, when the first 60 operational satellites were launched. The total cost of the decade-long project to design, build, and deploy the constellation is estimated by SpaceX to be about $10 billion, including nearly $900 million in Federal Communications Commission subsidies.
                </p>
