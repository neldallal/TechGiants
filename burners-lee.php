            <p>Main article: List of awards and honours received by Tim Berners-Lee
            Berners-Lee has received many awards and honours. He was knighted by Queen Elizabeth II in the 2004 New Year Honours "for services to the global development of the Internet", and was invested formally on 16 July 2004.</p>

            <p>On 13 June 2007, he was appointed to the Order of Merit (OM), an order restricted to 24 (living) members. Bestowing membership of the Order of Merit is within the personal purview of the Queen, and does not require recommendation by ministers or the Prime Minister. He was elected a Fellow of the Royal Society (FRS) in 2001. He has been conferred honorary degrees from a number of Universities around the world, including Manchester (his parents worked on the Manchester Mark 1 in the 1940s), Harvard and Yale.</p>

            <p>In 2012, Berners-Lee was among the British cultural icons selected by artist Sir Peter Blake to appear in a new version of his most famous artwork – the Beatles' Sgt. Pepper's Lonely Hearts Club Band album cover – to celebrate the British cultural figures of his life that he most admires to mark his 80th birthday.</p>

            <p>In 2013, he was awarded the inaugural Queen Elizabeth Prize for Engineering. On 4 April 2017, he received the 2016 ACM Turing Award "for inventing the World Wide Web, the first web browser, and the fundamental protocols and algorithms allowing the Web to scale".</p>

