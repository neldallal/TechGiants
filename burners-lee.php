            <h4 class="lee-h4">Personal life</h4>

            <div>Berners-Lee has said "I like to keep work and personal life separate".</div>

            <p>Berners-Lee married Nancy Carlson, an American computer programmer, in 1990. She was also working in Switzerland at the World Health Organization. They had two children and divorced in 2011. In 2014, he married Rosemary Leith at the Chapel Royal, St. James's Palace in London. Leith is a Canadian Internet and banking entrepreneur and a founding director of Berners-Lee's World Wide Web Foundation. The couple also collaborate on venture capital to support artificial intelligence companies.</p>

            <p>Berners-Lee was raised as an Anglican, but he turned away from religion in his youth. After he became a parent, he became a Unitarian Universalist (UU). When asked whether he believes in God, he stated: "Not in the sense of most people, I'm atheist and Unitarian Universalist".</p>

