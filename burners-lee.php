            <h4>Policy Work</h4>

            <p>Tim Berners-Lee at the Home Office, London, on 11 March 2010
            In June 2009, then-British prime minister Gordon Brown announced that Berners-Lee would work with the UK government to help make data more open and accessible on the Web, building on the work of the Power of Information Task Force. Berners-Lee and Professor Nigel Shadbolt are the two key figures behind data.gov.uk, a UK government project to open up almost all data acquired for official purposes for free re-use. Commenting on the opening up of Ordnance Survey data in April 2010, Berners-Lee said that: "The changes signal a wider cultural change in government based on an assumption that information should be in the public domain unless there is a good reason not to—not the other way around." He went on to say: "Greater openness, accountability and transparency in Government will give people greater choice and make it easier for individuals to get more directly involved in issues that matter to them."</p>


            <p>Berners-Lee speaking at the launch of the World Wide Web Foundation
            In November 2009, Berners-Lee launched the World Wide Web Foundation (WWWF) in order to campaign to "advance the Web to empower humanity by launching transformative programs that build local capacity to leverage the Web as a medium for positive change."</p>

            <p>Berners-Lee is one of the pioneer voices in favour of net neutrality and has expressed the view that ISPs should supply "connectivity with no strings attached” and should neither control nor monitor the browsing activities of customers without their expressed consent. He advocates the idea that net neutrality is a kind of human network right: "Threats to the Internet, such as companies or governments that interfere with or snoop on Internet traffic, compromise basic human network rights." Berners-Lee participated in an open letter to the US Federal Communications Commission (FCC). He and 20 other Internet pioneers urged the FCC to cancel a vote on 14 December 2017 to uphold net neutrality. The letter was addressed to Senator Roger Wicker, Senator Brian Schatz, Representative Marsha Blackburn and Representative Michael F. Doyle. Berners-Lee joined the board of advisors of start-up State.com, based in London. As of May 2012, Berners-Lee is president of the Open Data Institute, which he co-founded with Nigel Shadbolt in 2012.</p>

            <p>The Alliance for Affordable Internet (A4AI) was launched in October 2013 and Berners-Lee is leading the coalition of public and private organisations that includes Google, Facebook, Intel, and Microsoft. The A4AI seeks to make Internet access more affordable so that access is broadened in the developing world, where only 31% of people are online. Berners-Lee will work with those aiming to decrease Internet access prices so that they fall below the UN Broadband Commission's worldwide target of 5% of monthly income.</p>

            <p>Berners-Lee holds the founders chair in Computer Science at the Massachusetts Institute of Technology, where he heads the Decentralized Information Group and is leading Solid, a joint project with the Qatar Computing Research Institute that aims to radically change the way Web applications work today, resulting in true data ownership as well as improved privacy. In October 2016, he joined the Department of Computer Science at Oxford University as a professorial research fellow and as a fellow of Christ Church, one of the Oxford colleges.</p>

