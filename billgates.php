				<section class="box">

					<h4 class="windows-header">Management Style</h4>

					<figure class="figure3">
						<img class="flexible" src="images/gatesthird.jpg" alt="A photo of Bill Gates">
						<figcaption>Figure 3: A candid shot of Bill Gates</figcaption>
					</figure>

					<p>Gates had primary responsibility for Microsoft's product strategy from the company's founding in 1975 until 2006. He gained a reputation for being distant from others; an industry executive complained in 1981 that "Gates is notorious for not being reachable by phone and for not returning phone calls." An Atari executive recalled that he showed Gates a game and defeated him 35 of 37 times. When they met again a month later, Gates "won or tied every game. He had studied the game until he solved it. That is a competitor".</p>

					<p>Gates met regularly with Microsoft's senior managers and program managers, and the managers described him as being verbally combative. He also berated them for perceived holes in their business strategies or proposals that placed the company's long-term interests at risk. He interrupted presentations with such comments as "that's the stupidest thing I've ever heard" and "why don't you just give up your options and join the Peace Corps?" The target of his outburst would then have to defend the proposal in detail until Gates was fully convinced. When subordinates appeared to be procrastinating, he was known to remark sarcastically, "I'll do it over the weekend."</p>

					<p>During Microsoft's early years, Gates was an active software developer, particularly in the company's programming language products, but his primary role in most of the company's history was as a manager and executive. He has not officially been on a development team since working on the TRS-80 Model 100, but he wrote code that shipped with the company's products as late as 1989.Jerry Pournelle wrote in 1985 when Gates announced Microsoft Excel: "Bill Gates likes the program, not because it's going to make him a lot of money (although I'm sure it will do that), but because it's a neat hack."</p>

