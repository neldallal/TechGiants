				<section class="item1">

					<h4 class="gates-h4">Basic</h4>
					<p>Gates read the January 1975 issue of Popular Electronics which demonstrated the Altair 8800, and he contacted Micro Instrumentation and Telemetry Systems (MITS) to inform them that he and others were working on a BASIC interpreter for the platform. In reality, Gates and Allen did not have an Altair and had not written code for it; they merely wanted to gauge MITS's interest. MITS president Ed Roberts agreed to meet them for a demonstration, and over the course of a few weeks they developed an Altair emulator that ran on a minicomputer, and then the BASIC interpreter. The demonstration was held at MITS's offices in Albuquerque, New Mexico; it was a success and resulted in a deal with MITS to distribute the interpreter as Altair BASIC. MITS hired Allen, and Gates took a leave of absence from Harvard to work with him at MITS in November 1975. <em>Allen named their partnership "Micro-Soft", a combination of "microcomputer" and "software"</em>, and their first office was in Albuquerque. The first employee Gates and Allen hired was their high school collaborator Ric Weiland. They dropped the hyphen within a year and officially registered the trade name "Microsoft" with the Secretary of the State of New Mexico on November 26, 1976. Gates never returned to Harvard to complete his studies.</p>

					<p>Microsoft's Altair BASIC was popular with computer hobbyists, but Gates discovered that a pre-market copy had leaked out and was being widely copied and distributed. In February 1976, he wrote an Open Letter to Hobbyists in the MITS newsletter in which he asserted that more than 90% of the users of Microsoft Altair BASIC had not paid Microsoft for it and the Altair "hobby market" was in danger of eliminating the incentive for any professional developers to produce, distribute, and maintain high-quality software. This letter was unpopular with many computer hobbyists, but Gates persisted in his belief that software developers should be able to demand payment. Microsoft became independent of MITS in late 1976, and it continued to develop programming language software for various systems. The company moved from Albuquerque to Bellevue, Washington on January 1, 1979.</p>

					<p>Gates said he personally reviewed and often rewrote every line of code that the company produced in its first five years. As the company grew he transitioned into a manager role, then an executive.</p>

				</section>

				<section class="item2">	

					<h4 class="gates-h4">IBM Partnership</h4>

					<p>IBM, the leading supplier of computer equipment to commercial enterprises at the time, approached Microsoft in July 1980 concerning software for its upcoming personal computer, the IBM PC. IBM first proposed that Microsoft write the BASIC interpreter. IBM's representatives also mentioned that they needed an operating system, and Gates referred them to Digital Research (DRI), makers of the widely used CP/M operating system. IBM's discussions with Digital Research went poorly, however, and they did not reach a licensing agreement. IBM representative Jack Sams mentioned the licensing difficulties during a subsequent meeting with Gates and asked if Microsoft could provide an operating system. A few weeks later, Gates and Allen proposed using 86-DOS, an operating system similar to CP/M, that Tim Paterson of Seattle Computer Products (SCP) had made for hardware similar to the PC. Microsoft made a deal with SCP to be the exclusive licensing agent of 86-DOS, and later the full owner. Microsoft employed Paterson to adapt the operating system for the PC and delivered it to IBM as PC DOS for a one-time fee of $50,000.</p>

					<p>The contract itself only earned Microsoft a relatively small fee. It was the prestige brought to Microsoft by IBM's adoption of their operating system that would be the origin of Microsoft's transformation from a small business to the leading software company in the world. Gates had not offered to transfer the copyright on the operating system to IBM because he believed that other personal computer makers would clone IBM's PC hardware They did, making the IBM-compatible PC, running DOS, a de facto standard. The sales of MS-DOS (the version of DOS sold to customers other than IBM) made Microsoft a major player in the industry. The press quickly identified Microsoft as being very influential on the IBM PC. PC Magazine asked if Gates was "the man behind the machine?".</p>

					<p>Gates oversaw Microsoft's company restructuring on June 25, 1981, which re-incorporated the company in Washington state and made Gates the president and chairman of the board, with Paul Allen as vice president and vice chairman. In early 1983, Allen left the company after receiving a Hodgkin lymphoma diagnosis, effectively ending the formal business partnership between Gates and Allen, which had been strained months prior due to a contentious dispute over Microsoft equity. Later in the decade, Gates repaired his relationship with Allen and together the two donated millions to their childhood school Lakeside. They remained friends until Allen's death in October 2018.</p>

