				<h3>Post-Microsoft</h3>
				<div>
				<p>Since leaving day-to-day operations at Microsoft, Gates has continued his philanthropy and works on other projects.</p>

				<p>According to the Bloomberg Billionaires Index, <strong>Gates was the world's highest-earning billionaire in 2013</strong>, as his net worth increased by US$15.8 billion to US$78.5 billion. As of January 2014, most of Gates's assets are held in Cascade Investment LLC, an entity through which he owns stakes in numerous businesses, including Four Seasons Hotels and Resorts, and Corbis Corp. On February 4, 2014, Gates stepped down as chairman of Microsoft to become "technology advisor" alongside CEO Satya Nadella.</p>

				<p>Gates provided his perspective on a range of issues in a substantial interview that was published in the March 27, 2014 issue of Rolling Stone magazine. In the interview, Gates provided his perspective on climate change, his charitable activities, various tech companies and people involved in them, and the state of America. In response to a question about his greatest fear when he looks 50 years into the future, Gates stated: <em>"... there'll be some really bad things that'll happen in the next 50 or 100 years, but hopefully none of them on the scale of, say, a million people that you didn't expect to die from a pandemic, or nuclear or bioterrorism."</em> Gates also identified innovation as the "real driver of progress" and pronounced that <strong>"America's way better today than it's ever been."</strong></p>

				<p>Gates has expressed concern about the potential harms of superintelligence; in a Reddit "ask me anything", he stated that: </p>

					<blockquote>"First the machines will do a lot of jobs for us and not be super intelligent. That should be positive if we manage it well. A few decades after that though the intelligence is strong enough to be a concern. I agree with Elon Musk and some others on this and don't understand why some people are not concerned."</blockquote>

