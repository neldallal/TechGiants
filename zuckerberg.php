                    <p><strong>Mark Elliot Zuckerberg</strong> is an American media magnate, internet entrepreneur, and philanthropist. He is known for co-founding Facebook, Inc. and serves as its chairman, chief executive officer, and controlling shareholder. He also is a co-founder of the solar sail spacecraft development project Breakthrough Starshot and serves as one of its board members. Born in White Plains, New York, Zuckerberg attended Harvard University, where he launched the Facebook social networking service from his dormitory room on February 4, 2004, with college roommates Eduardo Saverin, Andrew McCollum, Dustin Moskovitz, and Chris Hughes. </p>
                    
                    <p>Zuckerberg began using computers and writing software in middle school. His father taught him Atari BASIC Programming in the 1990s, and later hired software developer David Newman to tutor him privately. Zuckerberg took a graduate course in the subject at Mercy College near his home while still in high school. In one program, since his father's dental practice was operated from their home, he built a software program he called "ZuckNet" that allowed all the computers between the house and dental office to communicate with each other. It is considered a "primitive" version of AOL's Instant Messenger, which came out the following year.</p>

                    <p>A New Yorker profile said of Zuckerberg: <em>"some kids played computer games. Mark created them."</em> Zuckerberg himself recalls this period: "I had a bunch of friends who were artists. They'd come over, draw stuff, and I'd build a game out of it." The New Yorker piece noted that Zuckerberg was not, however, a typical "geek-klutz", as he later became captain of his prep school fencing team and earned a classics diploma. Napster co-founder Sean Parker, a close friend, notes that Zuckerberg was "really into Greek odysseys and all that stuff", recalling how he once quoted lines from the Roman epic poem Aeneid, by Virgil, during a Facebook product conference.</p>
