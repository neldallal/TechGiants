                    <h3>The Social Network</h3>

                    <div>
                    <p>Jeff Jarvis, author of the book Public Parts, interviewed Zuckerberg and believed Sorkin made up too much of the story. He stated, "That's what the internet is accused of doing, making stuff up, not caring about the facts."</p>

                    <p>According to David Kirkpatrick, former technology editor at Fortune magazine and author of The Facebook Effect: The Inside Story of the Company That Is Connecting the World, (2011)</p> 

                        <blockquote>"the film is only "40% true ... he is not snide and sarcastic in a cruel way, the way Zuckerberg is played in the movie." He says that "a lot of the factual incidents are accurate, but many are distorted and the overall impression is false", and concludes that primarily "his motivations were to try and come up with a new way to share information on the Internet"</blockquote>

                    <p>Although the film portrayed Zuckerberg's creation of Facebook in order to elevate his stature after not getting into any of the elite final clubs at Harvard, Zuckerberg said he had no interest in joining the clubs. Kirkpatrick agreed that the impression implied by the film is "false". Karel Baloun, a former senior engineer at Facebook, noted that the "image of Zuckerberg as a socially inept nerd is overstated ... It is fiction ..." He likewise dismissed the film's assertion that he "would deliberately betray a friend."</p>
