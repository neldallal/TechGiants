                <section class="box-mark">
                <h4>Platform, Beacon, and Connect</h4>

                    <p>On May 24, 2007, Zuckerberg announced Facebook Platform, a development platform for programmers to create social applications within Facebook. Within weeks, many applications had been built and some already had millions of users. It grew to more than 800,000 developers around the world building applications for Facebook Platform. On November 6, 2007, Zuckerberg announced Beacon, a social advertising system that enabled people to share information with their Facebook friends based on their browsing activities on other sites. For example, eBay sellers could let friends know automatically what they have for sale via the Facebook news feed as they listed items for sale.</p>
            
                    <p>The program came under scrutiny because of privacy concerns from groups and individual users. Zuckerberg and Facebook failed to respond to the concerns quickly, and on December 5, 2007, Zuckerberg wrote a blog post on Facebook, taking responsibility for the concerns about Beacon and offering an easier way for users to opt out of the service. In 2007, Zuckerberg was added to MIT Technology Review's TR35 list as one of the top 35 innovators in the world under the age of 35. On July 23, 2008, Zuckerberg announced Facebook Connect, a version of Facebook Platform for users.</p>
