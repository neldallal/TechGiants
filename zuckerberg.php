                <section class="item1-mark">
                <h4 class="mark-h4">Facebook</h4>

                    <p>On February 4, 2004, Zuckerberg launched Facebook from his Harvard dormitory room. An earlier inspiration for Facebook may have come from Phillips Exeter Academy, the prep school from which Zuckerberg graduated in 2002. It published its own student directory, "The Photo Address Book", which students referred to as "The Facebook". Such photo directories were an important part of the student social experience at many private schools. With them, students were able to list attributes such as their class years, their friends, and their telephone numbers.</p>
                    
                    <p>Once at college, Zuckerberg's Facebook started off as just a "Harvard thing" until Zuckerberg decided to spread it to other schools, enlisting the help of roommate Dustin Moskovitz. They began with Columbia University, New York University, Stanford , Dartmouth, Cornell, University of Pennsylvania, Brown, and Yale. Samyr Laine, a triple jumper representing Haiti at the 2012 Summer Olympics, shared a room with Zuckerberg during Facebook's founding. "Mark was clearly on to great things," said Laine, who was Facebook's fourteenth user.</p>
            
                    <p>Zuckerberg, Moskovitz and some friends moved to Palo Alto, California, in Silicon Valley, where they leased a small house that served as an office. Over the summer, Zuckerberg met Peter Thiel, who invested in the company. They got their first office in mid-2004. According to Zuckerberg, the group planned to return to Harvard, but they eventually decided to remain in California. They had already turned down offers by major corporations to buy the company. In an interview in 2007, Zuckerberg explained his reasoning: <em>"It's not because of the amount of money. For me and my colleagues, the most important thing is that we create an open information flow for people. Having media corporations owned by conglomerates is just not an attractive idea to me."</em></p>
                </section>

                <section class="item2-mark">
                <h4 class="mark-h4">Internet.org</h4>

                    <p>In a public Facebook post, Zuckerberg launched the Internet.org project in late August 2013. He explained that the primary aim of the initiative is to provide Internet access to the five billion people who are not connected as of the launch date. According to Zuckerberg, Internet.org would also create new jobs and open up new markets using a three-tier strategy. He stated in his post. The world economy is going through a massive transition right now. The knowledge economy is the future. By bringing everyone online, we'll not only improve billions of lives, but we'll also improve our own as we benefit from the ideas and productivity they contribute to the world. Giving everyone the opportunity to connect is the foundation for enabling the knowledge economy. It is not the only thing we need to do, but it's a fundamental and necessary step.</p>
            
                    <p>Internet.org faced significant opposition in India, where activists said its limited internet ran counter to the idea of net neutrality; Zuckerberg said that a limited internet was better than no internet. Internet.org was shut down in India in February 2016. Zuckerberg later met with Narendra Modi, Satya Nadella and Sundar Pichai in the San Francisco Bay Area to discuss how to effectively establish affordable internet access in less developed countries. He also changed his Facebook profile picture to extend his support to Digital India to help rural communities stay connected to the internet.</p>

